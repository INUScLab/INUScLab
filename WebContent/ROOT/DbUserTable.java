package jsptable.dbtable;

public class DbUserTable{
	private int user_code;
	private String user_dong;
	private String user_gu;
	private String user_si;
	private String user_do;
	private int user_size;
	
	public int getUser_code() {
		return user_code;
	}
	public void setUser_code(int user_code) {
		this.user_code = user_code;
	}
	public String getUser_dong() {
		return user_dong;
	}
	public void setUser_dong(String user_dong) {
		this.user_dong = user_dong;
	}
	public String getUser_gu() {
		return user_gu;
	}
	public void setUser_gu(String user_gu) {
		this.user_gu = user_gu;
	}
	public String getUser_si() {
		return user_si;
	}
	public void setUser_si(String user_si) {
		this.user_si = user_si;
	}
	public String getUser_do() {
		return user_do;
	}
	public void setUser_do(String user_do) {
		this.user_do = user_do;
	}
	public String getUser_size() {
		return user_size;
	}
	public void setUser_size(int user_size) {
		this.user_size = user_size;
	}
}